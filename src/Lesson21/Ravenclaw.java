package Lesson21;

public class Ravenclaw {
    private String name;
    private String faculty;
    private String mind;
    private String wisdomy;
    private String wit;
    private String creativity;
    private String witchcraft;
    private String transgress;

    public Ravenclaw(String name, String faculty, String mind, String wisdomy, String wit, String creativity, String witchcraft, String transgress) {
        this.name = name;
        this.faculty = faculty;
        this.mind = mind;
        this.wisdomy = wisdomy;
        this.wit = wit;
        this.creativity = creativity;
        this.witchcraft = witchcraft;
        this.transgress = transgress;
    }
}
